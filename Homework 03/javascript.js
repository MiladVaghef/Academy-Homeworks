function summation(number1, number2) {
  return number1 + number2;
}

console.log(summation(10, 4));

function subtraction(number1, number2) {
  return number1 - number2;
}

console.log(subtraction(10, 4));

function multiplation(number1, number2) {
  return number1 * number2;
}

console.log(multiplation(5, 5));
